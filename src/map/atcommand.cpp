static
ATCE atcommand_heal(Session *s, dumb_ptr<map_session_data> sd,
        ZString message)
{
    int hp = 0, sp = 0;

    extract(message, record<' '>(&hp, &sp));

    if (hp == 0 && sp == 0)
    {
        hp = sd->status.max_hp - sd->status.hp;
        sp = sd->status.max_sp - sd->status.sp;
    }
    else
    {
        if (hp > 0 && (hp > sd->status.max_hp || hp > (sd->status.max_hp - sd->status.hp)))
            // fix positiv overflow
            hp = sd->status.max_hp - sd->status.hp;
        else if (hp < 0 && (hp < -sd->status.max_hp || hp < (1 - sd->status.hp)))
            // fix negativ overflow
            hp = 1 - sd->status.hp;
        if (sp > 0 && (sp > sd->status.max_sp || sp > (sd->status.max_sp - sd->status.sp)))
            // fix positiv overflow
            sp = sd->status.max_sp - sd->status.sp;
        else if (sp < 0 && (sp < -sd->status.max_sp || sp < (1 - sd->status.sp)))
            // fix negativ overflow
            sp = 1 - sd->status.sp;
    }

    if (hp < 0)
        // display like damage
        clif_damage(sd, sd, gettick(), interval_t::zero(), interval_t::zero(), -hp, 0, DamageType::RETURNED, 0);

    if (hp != 0 || sp != 0)
    {
        pc_heal(sd, hp, sp);
        if (hp >= 0 && sp >= 0)
            clif_displaymessage(s, "HP, SP recovered.");
        else
            clif_displaymessage(s, "HP or/and SP modified.");
    }
    else
    {
        clif_displaymessage(s, "HP and SP are already with the good value.");
        return ATCE::RANGE;
    }

    return ATCE::OKAY;
}

static
ATCE atcommand_item(Session *s, dumb_ptr<map_session_data> sd,
        ZString message)
{
    XString item_name;
    int number = 0, item_id;
    struct item_data *item_data = NULL;
    int get_count, i;

    if (!extract(message, record<' ', 1>(&item_name, &number)))
    {
        clif_displaymessage(s,
                "Please, enter an item name/id (usage: @item <item name or ID> [quantity]).");
        return ATCE::USAGE;
    }

    if (number <= 0)
        number = 1;

    item_id = 0;
    if ((item_data = itemdb_searchname(item_name)) != NULL)
        item_id = item_data->nameid;
    else if (extract(item_name, &item_id) && (item_data = itemdb_exists(item_id)) != NULL)
        item_id = item_data->nameid;
    else
        item_id = 0;

    if (item_id >= 500)
    {
        get_count = number;
        if (item_data->type == ItemType::WEAPON
            || item_data->type == ItemType::ARMOR
            || item_data->type == ItemType::_7
            || item_data->type == ItemType::_8)
        {
            get_count = 1;
        }
        for (i = 0; i < number; i += get_count)
        {
            struct item item_tmp {};
            item_tmp.nameid = item_id;
            PickupFail flag;
            if ((flag = pc_additem(sd, &item_tmp, get_count))
                != PickupFail::OKAY)
                clif_additem(sd, 0, 0, flag);
        }
        clif_displaymessage(s, "Item created.");
    }
    else
    {
        clif_displaymessage(s, "Invalid item ID or name.");
        return ATCE::EXIST;
    }

    return ATCE::OKAY;
}

